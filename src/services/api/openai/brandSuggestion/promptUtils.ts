
// Генерирует промпт для API OpenAI для получения предложений брендов
export function generateBrandSuggestionPrompt(description: string): string {
  return `Ты — эксперт по брендам и товарам. Мне нужно 5 популярных брендов, соответствующих запросу: "${description}".

ВАЖНО: Верни свой ответ ИСКЛЮЧИТЕЛЬНО в формате JSON-массива. Каждый элемент массива должен содержать следующие поля:
- brand: название бренда (реальное, существующее)
- product: конкретный популярный товар этого бренда, соответствующий запросу
- description: краткое описание товара (1 предложение)

Пример ожидаемого формата JSON:
[
  {"brand": "Nike", "product": "Air Zoom Pegasus 38", "description": "Легкие беговые кроссовки с амортизацией и поддержкой стопы"},
  {"brand": "Adidas", "product": "Ultraboost 22", "description": "Комфортные кроссовки с технологией Boost для длительных пробежек"}
]

Правила:
1. Название бренда должно быть реальным, существующим брендом
2. Название товара должно быть конкретным и соответствовать запросу
3. НЕ добавляй никаких пояснений, вступлений или заключений
4. Строго следуй указанному формату JSON
5. ВСЕГДА возвращай ровно 5 брендов
6. Для запросов на русском выдавай ответ на русском языке

Запрос пользователя: "${description}"`;
}
