
// Генерирует промпт для API OpenAI для получения предложений брендов
export function generateBrandSuggestionPrompt(description: string): string {
  return `Ты — эксперт по брендам и товарам. Мне нужно 5 популярных брендов, соответствующих запросу: "${description}".

ВАЖНО: Верни свой ответ ИСКЛЮЧИТЕЛЬНО в формате JSON. Ответ должен иметь следующую структуру:
{
  "suggestions": [
    {"brand": "Название бренда 1", "product": "Конкретный товар 1", "description": "Краткое описание товара 1"},
    {"brand": "Название бренда 2", "product": "Конкретный товар 2", "description": "Краткое описание товара 2"},
    {"brand": "Название бренда 3", "product": "Конкретный товар 3", "description": "Краткое описание товара 3"},
    {"brand": "Название бренда 4", "product": "Конкретный товар 4", "description": "Краткое описание товара 4"},
    {"brand": "Название бренда 5", "product": "Конкретный товар 5", "description": "Краткое описание товара 5"}
  ]
}

Правила:
1. Название бренда должно быть реальным, существующим брендом
2. Название товара должно быть конкретным и соответствовать запросу
3. НЕ добавляй никаких пояснений, вступлений или заключений
4. Строго следуй указанному формату JSON
5. ВСЕГДА возвращай ровно 5 брендов в массиве "suggestions"
6. Для запросов на русском выдавай ответ на русском языке

Запрос пользователя: "${description}"`;
}
