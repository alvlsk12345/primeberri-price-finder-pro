
// Генерирует промпт для API OpenAI для получения предложений брендов
export function generateBrandSuggestionPrompt(description: string): string {
  return `Ты эксперт по брендам и товарам. Назови 5 популярных брендов с конкретными товарами, которые могут соответствовать запросу: '${description}'. 

ОЧЕНЬ ВАЖНО: Твой ответ должен быть строго в формате массива JSON.

Формат ответа должен быть таким:
[
  {"brand": "Название бренда 1", "product": "Название товара 1", "description": "Описание товара 1"},
  {"brand": "Название бренда 2", "product": "Название товара 2", "description": "Описание товара 2"},
  {"brand": "Название бренда 3", "product": "Название товара 3", "description": "Описание товара 3"},
  {"brand": "Название бренда 4", "product": "Название товара 4", "description": "Описание товара 4"},
  {"brand": "Название бренда 5", "product": "Название товара 5", "description": "Описание товара 5"}
]

Правила:
1. Используй только реальные бренды и товары
2. Для запросов на русском отвечай на русском языке
3. СТРОГО придерживайся указанного формата JSON как массив объектов
4. Не включай дополнительный текст до или после JSON
5. ВСЕГДА возвращай ровно 5 объектов в массиве
6. Не используй экранирование кавычек, используй UTF-8 кодировку`;
}
